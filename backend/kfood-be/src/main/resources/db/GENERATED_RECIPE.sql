CREATE TABLE GENERATED_RECIPE (
  ID           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  TITLE        VARCHAR2(200),
  TIME_MIN     NUMBER,
  RECIPE_JSON  CLOB,
  AUDIO_URL    VARCHAR2(300),
  SOURCE       VARCHAR2(40) DEFAULT 'gemini',
  CREATED_AT   TIMESTAMP DEFAULT SYSTIMESTAMP
);

-- 테이블 확인
SELECT COUNT(*) FROM GENERATED_RECIPE;
SELECT ID, TITLE, TIME_MIN, CREATED_AT FROM GENERATED_RECIPE ORDER BY ID DESC FETCH FIRST 5 ROWS ONLY;
